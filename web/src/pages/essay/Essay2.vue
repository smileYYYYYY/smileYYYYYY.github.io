　　与清明时节缠绵不断、淅淅沥沥的小雨来比，自己对于阳光的喜爱或许来的更真实点，坐在窗前望着雨后初晴的太阳光，透过窗户的温暖感知、像母亲爱抚的双手触摸，那双温暖又熟悉布满老茧的双手，曾带给自己儿时多久的安全感和温暖记载、还是那样的轻轻进入思绪中。窗外温和的太阳光，洒在了一株株葱郁充满着绿色的樟树上。葱郁的叶片上留下了晶莹剔透的小水珠，迎着阳光的微笑显得与其的明亮和跳跃、一朵朵葱郁的樟树叶如一个个被滋润的孩提一样、含苞待放的吮吸着甘露、在阳光的映衬下活灵活现。绿色、葱郁、生机，或许这些只有在春天才可以具体展示的词藻，它早已经不是某一种事物的代言，而是整个初春寄予这世间万物的最温情的写照。</p><p>

　　沉侵在温暖阳光的爱抚下，看着这一株株生长在南国的樟树、那葱郁的绿色、那洋溢着希冀的生机，所带给春天般最美丽的语言，思绪的舞姿仿佛早已经被拉扯着跑了很远，望着这一株株的樟树就犹如在故乡里生活的那一排排白杨树一样，有时我在想那一排排在北国天空下生活的白杨树不正如这一株株生长在南国的樟树一样吗？它们共同的美丽不都是属于春天吗？樟树有樟树的生活习性，它耐酸、耐潮湿、它选择了自己可以生存发芽的土壤，白杨树也有白杨树的风土人情，它耐旱、耐碱、它同样也选择了自己生活的土地和生存方式，虽是不一样的选择，虽是不一样的旋律，但它们依然有着一样的共性，它们都是属于春天这个最美的记忆，它们都是初春的孩子。</p><p>

