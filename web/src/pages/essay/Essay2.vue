　　其实对于这一点，我们人类又何尝不是如此呢？我们就犹如生长在不同环境下的樟树和白杨树一样，有着相近或近相同的密切联系，对于环境我们别无选择，同样对于现状我们也别无选择、或许是我们要求的太多、想的太多、才会烦恼的愈来俞多，只有真正的当我们可以静下心来，静下心来仔细回味的时候，或许其实每一天的我们也会使幸福的，同样是充实而富有蕴义的。
